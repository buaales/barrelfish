#define SHARED_REGION_ETH_RX_BASE (SHARED_REGION_BASE + SHARED_REGION_ETH_RX_FRAMES_OFFSET)

#define SHARED_REGION_VARIABLES_SIZE 0x1000
#define SHARED_REGION_VARIABLES_SIZE_BITS (4 + 4 + 4)
#define SHARED_REGION_CNI_MSG_SIZE 0x1000
#define SHARED_REGION_CNI_MSG_SIZE_BITS (4 + 4 + 4)
