#define SHARED_REGION_TTP_SIZE_BITS (4 + 4 + 4)
#define SHARED_REGION_ETH_SIZE 0x1000
#define SHARED_REGION_ETH_SIZE_BITS (4 + 4 + 4)

#endif /* SRC_INCLUDE_MEMORY_MAPS_H_ */
